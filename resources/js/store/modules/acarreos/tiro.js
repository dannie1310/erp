const URI = '/api/acarreos/tiro/';

export default {
    namespaced: true,
    state: {
        tiros: [],
        currentTiro: '',
        meta:{}
    },

    mutations: {
        SET_TIROS(state, data) {
            state.tiros = data;
        },
        SET_TIRO(state, data) {
            state.currentTiro = data;
        },
        SET_META(state, data) {
            state.meta = data;
        },
    },

    actions: {
        index(context, payload) {
            return new Promise((resolve, reject) => {
                axios
                    .get(URI, { params: payload.params })
                    .then(r => r.data)
                    .then(data => {
                        resolve(data);
                    })
                    .catch(error => {
                        reject(error);
                    });
            });
        },
        find(context, payload) {
            return new Promise((resolve, reject) => {
                axios
                    .get(URI + payload.id, { params: payload.params })
                    .then(r => r.data)
                    .then(data => {
                        resolve(data);
                    })
                    .catch(error => {
                        reject(error);
                    });
            });
        },
        paginate(context, payload) {
            return new Promise((resolve, reject) => {
                axios
                    .get(URI + 'paginate', { params: payload.params })
                    .then(r => r.data)
                    .then(data => {
                        resolve(data);
                    })
                    .catch(error => {
                        reject(error);
                    })
            })
        },
        agregarConcepto(context, payload) {
            console.log(payload.params)
            return new Promise((resolve, reject) => {
                swal({
                    title: "Eliminar Ajuste Negativo de Inventario",
                    text: "Â¿EstÃ¡ seguro de que desea eliminar este Ajuste de Inventario?",
                    icon: "warning",
                    closeOnClickOutside: false,
                    buttons: {
                        cancel: {
                            text: 'Cancelar',
                            visible: true
                        },
                        confirm: {
                            text: 'Si, Eliminar',
                            closeModal: false,
                        }
                    }
                })
                    .then((value) => {
                        if (value) {
                            axios
                                .delete(URI + payload.id, { params: payload.params })
                                .then(r => r.data)
                                .then(data => {
                                    swal("Ajuste de Inventario eliminado correctamente", {
                                        icon: "success",
                                        timer: 1500,
                                        buttons: false
                                    }).then(() => {
                                        resolve(data);
                                    })
                                })
                                .catch(error =>  {
                                    reject(error);
                                });
                        } else {
                            reject();
                        }
                    });
            });
        }
    },

    getters: {
        tiros(state) {
            return state.tiros
        },
        currentTiro(state) {
            return state.currentTiro
        },
        meta(state) {
            return state.meta;
        },

    }
}
